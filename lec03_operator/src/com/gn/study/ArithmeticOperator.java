package com.gn.study;

public class ArithmeticOperator {
	public static void main(String[] args) {
		
		// 1. 산술연산자
		int su1 = 10;
		int su2 = 3;
		System.out.println(su1+su2);
		System.out.println(su1-su2);
		System.out.println(su1*su2);
		System.out.println(su1/su2);
		System.out.println(su1%su2); //나머지값
		
		// 2. 복합 대입 연산자
		
	}

}
